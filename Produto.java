package user;

public class Produto {
	
	String nome;
	String categoria;
	String marca;
	
	
	public Produto(String nome, String categoria, String marca) {
		this.nome = nome;
		this.categoria = categoria;
		this.marca = marca;
	}





	public String getNome() {
		return nome;
	}


	public String getCategoria() {
		return categoria;
	}


	public String getMarca() {
		return marca;
	}
	
	
	
	

}
